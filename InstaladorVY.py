def suma(a: int, b: int) -> int:
    """
    Calcula la suma de dos números.

    Args:
        a (int): Primer número.
        b (int): Segundo número.

    Returns:
        int: La suma de a y b.
    """
    return a + b

if __name__ == "__main__":
    resultado = suma(5, 3)
    print("La suma es:", resultado)
